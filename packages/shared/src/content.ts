/**
 * Sound Platform — Audio Content Schema
 * ========================================
 * Phase:   8-C (Complete Audio Creation Flow Foundation)
 * Updated: 2026-05-27
 *
 * This is the AUDIO CONTENT CORE MODULE — not generic social content.
 * `contentItems/{contentId}` is the unified storage collection, but the
 * schema is audio-first: uploaded audio, recorded audio, podcasts,
 * short clips, songs, album tracks, radio moments, competition submissions.
 *
 * Access control is determined by:
 *   - published status (visible only when status == 'published')
 *   - privacy/audience settings (owner-controlled)
 *   - moderation state (moderator-controlled)
 *   - world destination (controls PUBLISHING, NOT viewing)
 *
 * IMPORTANT: World destination does NOT gate viewing.
 *   - Plus content is readable by ANY authenticated user if published.
 *   - World controls PUBLISHING capabilities only.
 *   - Audio files are always served via signed URLs (Cloud Function).
 *
 * Phase 8-B will implement:
 *   - Audio upload/session pipeline
 *   - Signed URL generation
 *   - Waveform generation
 *   - Transcript/caption generation
 *   - Audio transcoding/normalization
 */

import type { WorldId } from './permissions';

// ─── Audio Content Status ────────────────────────────────────────────────────
//
// Lifecycle:
//   draft → readyForUpload → uploading → processing → published
//   Any state → rejected | archived | deleted
//
export type AudioContentStatus =
  | 'draft'
  | 'readyForUpload'
  | 'uploading'
  | 'processing'
  | 'published'
  | 'rejected'
  | 'archived'
  | 'deleted';

// ─── Audio Content Kind ──────────────────────────────────────────────────────
//
// Audio-first content types across all worlds:
//   general/plus:  longAudio, podcast, shortAudio
//   music:         song, albumTrack
//   radio:         radioMoment (station system only, not normal publishing)
//   tournaments:   tournamentSubmissionAudio (competition flow, not normal publishing)
//
export type AudioContentKind =
  | 'longAudio'
  | 'podcast'
  | 'shortAudio'
  | 'song'
  | 'albumTrack'
  | 'radioMoment'
  | 'tournamentSubmissionAudio';

// ─── Audio Moderation Status ─────────────────────────────────────────────────
export type AudioModerationStatus =
  | 'pending'
  | 'approved'
  | 'rejected'
  | 'flagged'
  | 'underReview';

// ─── Audio Asset Metadata ────────────────────────────────────────────────────
//
// Embedded inside AudioContentDoc as `audioAsset`.
// One audio item = one audio asset (1:1 relationship).
//
// Phase 8-B will implement:
//   - storagePath         → set by upload pipeline
//   - uploadSessionId     → tracks resumable uploads
//   - processingStatus    → set by transcoding pipeline
//   - waveformStatus      → set by waveform generator
//   - transcriptStatus    → set by transcription pipeline
//   - playbackUrl         → signed URL generated by Cloud Function
//
export interface AudioAssetMeta {
  /** Unique asset identifier (server-generated) */
  assetId: string;

  /** Cloud Storage path — NEVER exposed to client directly */
  storagePath?: string;

  /** Original file name from upload */
  originalFileName?: string;

  /** MIME type (e.g. 'audio/mpeg', 'audio/wav', 'audio/aac') */
  mimeType?: string;

  /** File size in bytes */
  sizeBytes?: number;

  /** Duration in milliseconds */
  durationMs?: number;

  // ── Processing pipeline fields (Phase 8-B) ───────────────────────────────

  /** Waveform generation status */
  waveformStatus?: 'pending' | 'processing' | 'done' | 'failed';
  /** Cloud Storage path to generated waveform data */
  waveformPath?: string;

  /** Transcript/caption generation status */
  transcriptStatus?: 'pending' | 'processing' | 'done' | 'failed';
  /** Cloud Storage path to generated transcript */
  transcriptPath?: string;

  /** Overall audio processing status */
  processingStatus?: 'pending' | 'processing' | 'done' | 'failed';
  /** Error message if processing failed */
  processingError?: string;

  /**
   * Playback URL — always a signed URL generated by Cloud Function.
   * NEVER stored permanently; generated on demand.
   * This field exists as a placeholder for the callable response shape.
   */
  playbackUrl?: string;

  // ── Source & Upload tracking (Phase 8-B) ──────────────────────────────────

  /** How the audio was acquired */
  sourceType?: 'recorded' | 'uploaded';

  /** Client-tracked upload lifecycle */
  uploadStatus?: 'pending' | 'uploading' | 'uploaded' | 'failed';

  /** ISO timestamp of upload completion */
  uploadedAt?: string;
}

