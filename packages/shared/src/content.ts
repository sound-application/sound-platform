// ─── Audio Content Document ──────────────────────────────────────────────────
//
// Stored in: contentItems/{contentId}
//
// This is the canonical audio content entity across all worlds.
// Each document represents one published (or in-progress) audio item.
//
export interface AudioContentDoc {
  /** Document ID (matches Firestore doc ID) */
  id: string;

  /** Firebase UID of the content owner */
  ownerUid: string;

  /** Denormalized owner info for feed rendering */
  owner: OwnerSnapshot;

  // ── Classification ─────────────────────────────────────────────────────────

  /** Target world — controls publishing permissions, NOT viewing */
  world: WorldId;

  /** Audio content kind */
  kind: AudioContentKind;

  // ── Metadata ───────────────────────────────────────────────────────────────

  title: string;
  caption?: string;
  description?: string;

  /** Category ID from admin-managed categories collection */
  categoryId?: string;
  /** Localized category label snapshot */
  categoryLabel?: string;
  /** Subcategory ID */
  subcategoryId?: string;
  /** Localized subcategory label snapshot */
  subcategoryLabel?: string;

  /** Country targeting mode (Phase 8-C) */
  countryMode?: CountryMode;
  /** Country codes when countryMode !== 'all' */
  countryCodes?: string[];
  /** @deprecated use countryMode/countryCodes instead */
  countryCode?: string;
  /** @deprecated */
  countryLabel?: string;

  /** ISO 639-1 language code */
  language?: string;

  /** User-defined tags for discovery */
  tags: string[];

  /** Age suitability (Phase 8-C) */
  ageSuitability?: AgeSuitability;

  /** Publish behaviour toggles (Phase 8-C) */
  publishToggles?: PublishToggles;

  /** Cover image asset (Phase 8-C) */
  coverAsset?: CoverAsset;

  /** Captions setup preferences (Phase 8-C) */
  captionsSetup?: CaptionsSetup;

  /** AutoCue teleprompter config (Phase 8-C) */
  autoCue?: AutoCueConfig;

  // ── Phase 8-D.2 Fields ─────────────────────────────────────────────────────

  /** Whether this is child-directed content */
  isChildContent?: boolean;

  /** Placement feed: main feed, shorts/clips, or both */
  placementFeed?: PlacementFeed;

  /** Playlist intent: none, add to existing, or create new */
  playlistIntent?: PlaylistIntent;
  /** Existing playlist ID when playlistIntent === 'existing' */
  playlistId?: string;
  /** New playlist name when playlistIntent === 'new' */
  newPlaylistName?: string;

  // ── Visibility & Status ────────────────────────────────────────────────────

  /** Audience/privacy setting: 'public' | 'followers' | 'friends' | 'onlyMe' | 'custom' */
  audience: string;

  /** Content lifecycle status */
  status: AudioContentStatus;

  /** Moderation review status (server-only writes) */
  moderationStatus: AudioModerationStatus;

  /** Explicit content flag */
  isExplicit: boolean;

  // ── Audio Asset ────────────────────────────────────────────────────────────

  /** Embedded audio asset metadata (1:1 with content item) */
  audioAsset?: AudioAssetMeta;

  // ── Processing Pipeline (Phase 8-E) ────────────────────────────────────────

  /** Overall processing pipeline status */
  contentProcessingStatus?: ContentProcessingStatus;
  /** ISO timestamp — when processing was queued/started */
  processingStartedAt?: string;
  /** ISO timestamp — when processing completed (success or failure) */
  processingCompletedAt?: string;

  /** Processed/transcoded audio output — null until real transcoding runs */
  processedAudio?: ProcessedAudioMeta;

  /** Waveform peaks data (may be synthetic placeholder) */
  waveform?: WaveformData;

  /** Captions/transcription processing status (separate from captionsSetup) */
  captionsProcessing?: CaptionsProcessing;

  /** Captions asset metadata — null until real transcription produces output */
  captionsAsset?: CaptionsAsset;

  /** Creator-authored captions data (Phase 8-H.1) — inline, no provider needed */
  captionsData?: CaptionsData;

  /** Audio effects configuration (Phase 8-J) — presets or manual filters */
  effectsConfig?: AudioEffectsConfig;

  // ── Counters (server-only writes via Cloud Functions) ──────────────────────

  listensCount: number;
  likesCount: number;
  savesCount: number;
  repostsCount: number;
  commentsCount: number;
  sharesCount: number;

  // ── Timestamps ─────────────────────────────────────────────────────────────

  /** ISO timestamp — when the content was first created */
  createdAt: string;
  /** ISO timestamp — last metadata update */
  updatedAt: string;
  /** ISO timestamp — when status became 'published' */
  publishedAt?: string;

  // ── Relations (optional, for future modules) ───────────────────────────────

  /** Parent content ID — for shorts cut from long audio */
  parentContentId?: string;
  /** Source audio ID — if derived from another audio item */
  sourceAudioId?: string;
  /** Album ID — for album tracks (music world) */
  albumId?: string;
  /** Competition ID — for tournament submissions */
  competitionId?: string;
  /** Radio station ID — for radio moments */
  radioStationId?: string;
}

