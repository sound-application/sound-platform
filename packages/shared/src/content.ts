// ─── Owner Snapshot ──────────────────────────────────────────────────────────
//
// Denormalized from publicProfiles/{uid} at publish time.
// Allows feed rendering without joining to profiles collection.
// Updated by Cloud Function if owner profile changes (Phase 8-B+).
//
export interface OwnerSnapshot {
  ownerUsername: string;
  ownerDisplayName: string;
  ownerAvatarUrl?: string;
}

// ─── Audio Content Document ──────────────────────────────────────────────────
//
// Stored in: contentItems/{contentId}
//
// This is the canonical audio content entity across all worlds.
// Each document represents one published (or in-progress) audio item.
//
export interface AudioContentDoc {
  /** Document ID (matches Firestore doc ID) */
  id: string;

  /** Firebase UID of the content owner */
  ownerUid: string;

  /** Denormalized owner info for feed rendering */
  owner: OwnerSnapshot;

  // ── Classification ─────────────────────────────────────────────────────────

  /** Target world — controls publishing permissions, NOT viewing */
  world: WorldId;

  /** Audio content kind */
  kind: AudioContentKind;

  // ── Metadata ───────────────────────────────────────────────────────────────

  title: string;
  caption?: string;
  description?: string;

  /** Category ID from admin-managed categories collection */
  categoryId?: string;
  /** Localized category label snapshot */
  categoryLabel?: string;
  /** Subcategory ID */
  subcategoryId?: string;
  /** Localized subcategory label snapshot */
  subcategoryLabel?: string;

  /** Country targeting mode (Phase 8-C) */
  countryMode?: CountryMode;
  /** Country codes when countryMode !== 'all' */
  countryCodes?: string[];
  /** @deprecated use countryMode/countryCodes instead */
  countryCode?: string;
  /** @deprecated */
  countryLabel?: string;

  /** ISO 639-1 language code */
  language?: string;

  /** User-defined tags for discovery */
  tags: string[];

  /** Age suitability (Phase 8-C) */
  ageSuitability?: AgeSuitability;

  /** Publish behaviour toggles (Phase 8-C) */
  publishToggles?: PublishToggles;

  /** Cover image asset (Phase 8-C) */
  coverAsset?: CoverAsset;

  /** Captions setup preferences (Phase 8-C) */
  captionsSetup?: CaptionsSetup;

  /** AutoCue teleprompter config (Phase 8-C) */
  autoCue?: AutoCueConfig;

  // ── Phase 8-D.2 Fields ─────────────────────────────────────────────────────

  /** Whether this is child-directed content */
  isChildContent?: boolean;

  /** Placement feed: main feed, shorts/clips, or both */
  placementFeed?: PlacementFeed;

  /** Playlist intent: none, add to existing, or create new */
  playlistIntent?: PlaylistIntent;
  /** Existing playlist ID when playlistIntent === 'existing' */
  playlistId?: string;
  /** New playlist name when playlistIntent === 'new' */
  newPlaylistName?: string;

  // ── Visibility & Status ────────────────────────────────────────────────────

  /** Audience/privacy setting: 'public' | 'followers' | 'friends' | 'onlyMe' | 'custom' */
  audience: string;

  /** Content lifecycle status */
  status: AudioContentStatus;

  /** Moderation review status (server-only writes) */
  moderationStatus: AudioModerationStatus;

  /** Explicit content flag */
  isExplicit: boolean;

  // ── Audio Asset ────────────────────────────────────────────────────────────

  /** Embedded audio asset metadata (1:1 with content item) */
  audioAsset?: AudioAssetMeta;

  // ── Counters (server-only writes via Cloud Functions) ──────────────────────

  listensCount: number;
  likesCount: number;
  savesCount: number;
  repostsCount: number;
  commentsCount: number;
  sharesCount: number;

  // ── Timestamps ─────────────────────────────────────────────────────────────

  /** ISO timestamp — when the content was first created */
  createdAt: string;
  /** ISO timestamp — last metadata update */
  updatedAt: string;
  /** ISO timestamp — when status became 'published' */
  publishedAt?: string;

  // ── Relations (optional, for future modules) ───────────────────────────────

  /** Parent content ID — for shorts cut from long audio */
  parentContentId?: string;
  /** Source audio ID — if derived from another audio item */
  sourceAudioId?: string;
  /** Album ID — for album tracks (music world) */
  albumId?: string;
  /** Competition ID — for tournament submissions */
  competitionId?: string;
  /** Radio station ID — for radio moments */
  radioStationId?: string;
}

// ─── Audio Draft Document ────────────────────────────────────────────────────
//
// Stored in: drafts/{uid}/drafts/{draftId}
//
// Supports the SRS rule: "Audio Information screen comes first.
// If user skips publish steps, content is saved as draft automatically."
//
// All content fields are optional because the draft starts nearly empty
// and is progressively filled through the creation wizard.
//
export interface AudioDraftDoc {
  /** Draft document ID (server-generated) */
  draftId: string;

  /** Firebase UID of the draft owner */
  ownerUid: string;

  // ── Classification (optional until publish) ────────────────────────────────

  /** Target world — optional during drafting */
  world?: WorldId;
  /** Audio content kind — optional during drafting */
  kind?: AudioContentKind;

  // ── Metadata (progressively filled) ────────────────────────────────────────

  title?: string;
  caption?: string;
  description?: string;

  categoryId?: string;
  categoryLabel?: string;
  subcategoryId?: string;
  subcategoryLabel?: string;
  countryMode?: CountryMode;
  countryCodes?: string[];
  countryCode?: string;
  countryLabel?: string;
  language?: string;
  tags?: string[];

  audience?: string;
  isExplicit?: boolean;
  ageSuitability?: AgeSuitability;
  publishToggles?: PublishToggles;
  coverAsset?: CoverAsset;
  captionsSetup?: CaptionsSetup;
  autoCue?: AutoCueConfig;

  /** Whether this is child-directed content (Phase 8-D.2) */
  isChildContent?: boolean;
  /** Placement feed (Phase 8-D.2) */
  placementFeed?: PlacementFeed;
  /** Playlist intent (Phase 8-D.2) */
  playlistIntent?: PlaylistIntent;
  /** Existing playlist ID (Phase 8-D.2) */
  playlistId?: string;
  /** New playlist name (Phase 8-D.2) */
  newPlaylistName?: string;

  // ── Draft Status ───────────────────────────────────────────────────────────

  /** Draft lifecycle: 'draft' (editing) or 'readyForUpload' (info complete) */
  status: 'draft' | 'readyForUpload';

  /** Links to published contentItem if editing an existing item */
  targetContentId?: string;

  /** Audio asset ID if audio has been associated (Phase 8-B) */
  audioAssetId?: string;

  /** Embedded audio asset metadata — attached after recording/upload (Phase 8-B) */
  audioAsset?: AudioAssetMeta;

  /** UI creation wizard step tracker — e.g. 'info', 'record', 'effects', 'publish' */
  currentStep?: string;

  // ── Timestamps ─────────────────────────────────────────────────────────────

  createdAt: string;
  updatedAt: string;
  lastSavedAt: string;
}

